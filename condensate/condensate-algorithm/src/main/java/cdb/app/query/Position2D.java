public class Position2D {

    /** value in x-axis*/
    public int x;
    /** value in y-axis*/
    public int y;

    /**
     * Construction
     * 
     * @param x     value in x-axis
     * @param y     value in y-axis
     */
    public Position2D(int x, int y) {
        this.x = x;
        this.y = y;
    }

}
